    private void userTablejTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTablejTableMouseClicked
        DefaultTableModel userTable = (DefaultTableModel)userTablejTable.getModel();
        int selectedRow = userTablejTable.getSelectedRow();
        String selectedUserPointer = (userTable.getValueAt(selectedRow, 0).toString());
        int selectedUserID = Integer.parseInt(selectedUserPointer);			
        List resultSetArrayList = user.setUserDetails(selectedUserID);
            
            userIDjTextField.setText(resultSetArrayList.get(0).toString());
            userFirstnamejTextField.setText(resultSetArrayList.get(1).toString());
            userMiddlenamejTextField.setText(resultSetArrayList.get(2).toString());
            userLastnamejTextField.setText(resultSetArrayList.get(3).toString());
           
            switch (resultSetArrayList.get(4).toString()) {
            case "Male":
                userGenderjComboBox.setSelectedItem("Male");
                break;
            case "Female":
                userGenderjComboBox.setSelectedItem("Female");
                break;
             case "Other":
                userGenderjComboBox.setSelectedItem("Other");
                break;
            default:
                userGenderjComboBox.setSelectedItem("User Gender:");
                break;
        }
            userDOBjTextField.setText(resultSetArrayList.get(5).toString());
           
            
            switch (resultSetArrayList.get(6).toString()) {
            case "Admin":
                userTypejComboBox.setSelectedItem("Admin");
                break;
            case "Case Worker":
                userTypejComboBox.setSelectedItem("Case Worker");
                break;
            default:
                userTypejComboBox.setSelectedItem("User Type:");
                break;
            }
            userUsernamejTextField.setText(resultSetArrayList.get(7).toString());
            userPasswordjTextField.setText(resultSetArrayList.get(8).toString());
            userEmailjTextField.setText(resultSetArrayList.get(9).toString());
            userTeljTextField.setText(resultSetArrayList.get(10).toString());
            userMobjTextField.setText(resultSetArrayList.get(10).toString());
            userWorkTeljTextField.setText(resultSetArrayList.get(12).toString());
            userAddr1jTextField.setText(resultSetArrayList.get(13).toString());
            userAddr2jTextField.setText(resultSetArrayList.get(14).toString());
            userPostcodejTextField.setText(resultSetArrayList.get(15).toString());
            userNotesjTextArea.setText(resultSetArrayList.get(16).toString()); 
    }//GEN-LAST:event_userTablejTableMouseClicked

